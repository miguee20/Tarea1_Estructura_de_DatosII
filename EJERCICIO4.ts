// for para repetir 5 veces
for (let i = 1; i <= 5; i++) {
    console.log(`REPETIR NUMERO ${i}`);
}

// while para repetir mientras el contador sea mmenor o igual que 5
let contador: number = 1;

while (contador <= 5) {
    console.log(`REPETIR NUMERO ${contador}`);
    contador++;
}    